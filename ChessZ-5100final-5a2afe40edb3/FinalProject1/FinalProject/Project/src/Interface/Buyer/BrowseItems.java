    public void populateTable() {
        int rowCount = tblBrowseItems.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)tblBrowseItems.getModel();
        dtm.setRowCount(0);
        
        for(int i=rowCount - 1; i>0; i--) {
            dtm.removeRow(i);
        }
        
        for (Item item : system.getItemDirectory().getItemDirectory()){
            Object[] row = new Object[3];
            row[0] = item;
            row[1] = item.getStartPrice();
            row[2] = item.getCurrentPrice();
            dtm.addRow(row);
        }
    }

