    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) auctionplatformJTable.getModel();

        model.setRowCount(0);
        for (Market market : system.getMarketList()) {
            for (AuctionPlatform auctionplatform : market.getAuctionplatformDirectory().getAuctionplatformList()) {
                for (UserAccount userAccount : auctionplatform.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = auctionplatform.getName();
                    row[1] = market.getName();
                    row[2] = userAccount.getUsername();

                    model.addRow(row);
                }
            }
        }
    }

    private void populateMarketComboBox(){
        marketJComboBox.removeAllItems();
        
        for (Market market : system.getMarketList()){
            marketJComboBox.addItem(market);
        }
    }
    
    private void populateAuctionPlatformComboBox(Market market){
        marketJComboBox.removeAllItems();
        
        for (AuctionPlatform auctionplatform : market.getAuctionplatformDirectory().getAuctionplatformList()){
            marketJComboBox.addItem(auctionplatform);
        }
        
    }
    
