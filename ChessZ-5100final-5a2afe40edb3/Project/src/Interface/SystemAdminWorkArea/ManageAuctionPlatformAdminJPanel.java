        String name = nameJTextField.getText();
        
        Employee employee = auctionplatform.getEmployeeDirectory().createEmployee(name);
        
        UserAccount useraccount = auctionplatform.getUserAccountDirectory().createUserAccount(username, password, employee, new AdminRole());
        populateTable();
        
